public class CreatingVariables {
    public static void main( String[] args ) {
        int x, y, age;
        double seconds, e, checking;
        String firstName, lastName, title;

        x = 10;
        y = 400;
        age = 39;

        seconds = 4.71;
        e = 2.71828182845904523536;
        checking = 1.89;

        firstName = "Graham";
        lastName = "Mitchell";
        title = "Mr.";

        System.out.println( "The variable x contains " + x );
    }
}
